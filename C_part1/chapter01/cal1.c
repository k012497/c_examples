#include <stdio.h>

int main () 
{
    int i = 3;
    int j = 5;

    printf("%d 더하기 %d 는 %d 입니다\n", i, j, i+j);
    printf("%d 더하기 %d 는 %d 입니다\n", i, j, i+j);
    printf("%d - %d = %d 입니다\n", i, j, i-j);
    printf("%d - %d = %d 입니다 \n", i, j, i-j);

    return 0;
}